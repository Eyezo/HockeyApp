package com.example.eyezo.hockeyapp;

public class Article {

    private String title;
    private String link;

    public Article()
    {

    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}